    public static Matcher<String> sameHexValue(final String expected, final Fixture fixture) {
        return new TypeSafeMatcher<String>() {
            @Override
            public boolean matchesSafely(String actual) {
                if (expected.equals(actual)) {
                    return true;
                }
                return false;
            }

            @Override
            public void describeTo(Description description) {
                description.appendValue(expected);
                description.appendText(" on ");
                description.appendText(fixture.toString());
            }
        };
    }

    static byte[] shortToBytes(int n) {
        byte hi = (byte)(n >> 8);
        byte lo = (byte)(n & 0xff);
        return new byte[] {hi, lo};
    }

    static class Fixture {
        public String carrier;
        public int sjis;
        public int unicode;
        public int androidPUA;
        public int unicode6;
        public int variationSelector;
        public int combining;
        public Fixture(String carrier, int sjis, int unicode, int androidPUA, int unicode6, int variationSelector, int combining) {
            this.carrier = carrier;
            this.sjis = sjis;
            this.unicode = unicode;
            this.androidPUA = androidPUA;
            this.unicode6 = unicode6;
            this.variationSelector = variationSelector;
            this.combining = combining;
        }
        public String toString() {
            return String.format("<Fixture: carrier: %s, sjis: 0x%X>", carrier, sjis);
        }
    }

    @Theory
    public void testGetEmojiForCodePoint_sjis(Fixture fixture) throws Exception {
        assumeThat(fixture.carrier, is("docomo"));

        String charset = "MS932";
        assumeTrue(Charset.isSupported(charset));

        byte[] ba_sjis = shortToBytes(fixture.sjis);
        InputStream in = new ByteArrayInputStream(ba_sjis);
        assertThat(in.available(), is(2));

        String unicode = IOUtils.toString(in, charset);

        assertThat(unicode.codePointCount(0, unicode.length()), is(1));
        assertThat(Integer.toHexString(unicode.codePointAt(0)), is(Integer.toHexString(fixture.unicode)));
    }

    @Theory
    public void testGetEmojiForCodePoint_sjis_2007(Fixture fixture) throws Exception {
        String carrier = fixture.carrier;
        String charset = "x-" + carrier + "-shift_jis-2007";
        assumeTrue(Charset.isSupported(charset));

        byte[] ba_sjis = shortToBytes(fixture.sjis);
        InputStream in = new ByteArrayInputStream(ba_sjis);
        assertThat(in.available(), is(2));

        String unicode = IOUtils.toString(in, charset);

        if (unicode.codePointAt(0) != fixture.androidPUA) {
            System.err.println(String.format("0x%X != 0x%X on %s", unicode.codePointAt(0), fixture.androidPUA, fixture));
        }
        assertThat(unicode.codePointCount(0, unicode.length()), is(1));
        assertThat(Integer.toHexString(unicode.codePointAt(0)), is(sameHexValue(Integer.toHexString(fixture.androidPUA), fixture)));
    }

    @Theory
    public void testGetEmojiForCodePoint_sjis_2012(Fixture fixture) throws Exception {
        String carrier = fixture.carrier;
        String charset = "x-" + carrier + "-shift_jis-2012";
        assumeTrue(Charset.isSupported(charset));

        byte[] ba_sjis = shortToBytes(fixture.sjis);
        InputStream in = new ByteArrayInputStream(ba_sjis);
        assertThat(in.available(), is(2));

        String unicode = IOUtils.toString(in, charset);

        if (fixture.combining > 0) {
            int i = 0;
            int c;

            c = unicode.codePointAt(i);
            assertThat(Integer.toHexString(c), is(sameHexValue(Integer.toHexString(fixture.unicode6), fixture)));
            i += Character.charCount(c);

            if (fixture.variationSelector > 0) {
                c = unicode.codePointAt(i);
                assertThat(Integer.toHexString(c), is(sameHexValue(Integer.toHexString(fixture.variationSelector), fixture)));
                i += Character.charCount(c);
            }

            c = unicode.codePointAt(i);
            assertThat(Integer.toHexString(c), is(sameHexValue(Integer.toHexString(fixture.combining), fixture)));
            i += Character.charCount(c);

            assertThat(unicode.length(), is(i));
        } else {
            assertThat(unicode.codePointCount(0, unicode.length()), is(1));
            assertThat(Integer.toHexString(unicode.codePointAt(0)), is(sameHexValue(Integer.toHexString(fixture.unicode6), fixture)));
        }
    }

